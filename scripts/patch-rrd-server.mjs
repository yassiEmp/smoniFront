// react-router-dom v7 dropped the /server subpath. vite-react-ssg still
// imports it. This script writes a tiny re-export shim into every resolved
// react-router-dom location under node_modules/.pnpm so SSR works.
//
// Runs from package.json postinstall.

import { readdirSync, statSync, writeFileSync, readFileSync, existsSync } from "node:fs";
import { join } from "node:path";

const SERVER_JS = `// Auto-generated by scripts/patch-rrd-server.mjs.
// react-router-dom v7 dropped the /server subpath; vite-react-ssg still
// imports it during SSR. Re-export from the main entry.
export {
  StaticRouter,
  StaticRouterProvider,
  createStaticHandler,
  createStaticRouter,
} from "./dist/index.mjs";
`;

function findRrdDirs(root) {
  const out = [];
  if (!existsSync(root)) return out;
  for (const name of readdirSync(root)) {
    if (!name.startsWith("react-router-dom@")) continue;
    const inner = join(root, name, "node_modules", "react-router-dom");
    try {
      if (statSync(inner).isDirectory()) out.push(inner);
    } catch {
      /* skip */
    }
  }
  return out;
}

const dirs = findRrdDirs("node_modules/.pnpm");
let patched = 0;
for (const dir of dirs) {
  const serverPath = join(dir, "server.js");
  const pkgPath = join(dir, "package.json");
  try {
    writeFileSync(serverPath, SERVER_JS);
    const pkg = JSON.parse(readFileSync(pkgPath, "utf8"));
    pkg.exports = pkg.exports ?? {};
    pkg.exports["./server"] = "./server.js";
    pkg.exports["./server.js"] = "./server.js";
    writeFileSync(pkgPath, JSON.stringify(pkg, null, 2) + "\n");
    patched++;
  } catch (err) {
    console.warn(`[patch-rrd-server] skip ${dir}:`, err.message);
  }
}
console.log(`[patch-rrd-server] patched ${patched} react-router-dom location(s).`);
